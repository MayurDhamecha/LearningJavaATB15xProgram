package ex_01_Java_Basics;

public class Lab002_Datatypes_And_Variables {
    public static void main(String[] args) {

        // Local Variables - Which only define within a method/function
        int int_Variable = 10;
        System.out.println("Byte variable value is : " + int_Variable);
        short short_Variable = 20;
        System.out.println("Byte variable value is : " + short_Variable);
        long long_Variable = 30;
        System.out.println("Byte variable value is : " + long_Variable);
        float float_Variable = 40;
        System.out.println("Byte variable value is : " + float_Variable);
        double double_Variable = 50;
        System.out.println("Byte variable value is : " + double_Variable);
        boolean boolean_Variable = true;
        System.out.println("Byte variable value is : " + boolean_Variable);
        char char_Variable = 'a';
        System.out.println("Byte variable value is : " + char_Variable);
        byte byte_Variable = 60;
        System.out.println("Byte variable value is : " + byte_Variable);
    }
}